"""Shared runtime primitives for OmicsClaw adapters.

After the P3 boundary restructure, ``runtime/`` is split into 5
sub-packages by concern:

  ``agent/``    — multi-round LLM dispatch loop (query_engine)
  ``context/``  — prompt-context assembly, compaction, budgets, layers
  ``tools/``    — tool spec/registry, execution, hooks, concrete builders
  ``policy/``   — tool approval, condition matching, workspace verification
  ``storage/``  — transcript / tool-result / task stores

This module re-exports the public surface so downstream callers can keep
using ``from omicsclaw.runtime import X`` without caring which sub-package
``X`` actually lives in.
"""

